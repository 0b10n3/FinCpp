#include <iostream>

int main()
{
    // Integer data types
    // int
    int numberOfStudents = 10;
    int numberOfTeachers = 3;

    std::cout << "Number of students: " << numberOfStudents << std::endl;
    std::cout << "Number of teachers: " << numberOfTeachers << std::endl;
    std::cout << "Teachers to students ratio: " << numberOfTeachers / numberOfStudents << std::endl;

    std::cout << "Hello World!" << std::endl;

    return 0;

}
